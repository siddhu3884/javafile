import java.util.ArrayList;

public class SimpleArrayListExample {
    public static void main(String[] args) {
        // Create an ArrayList
        ArrayList<Integer> numbers = new ArrayList<>();

        // Add some numbers
        numbers.add(10);
        numbers.add(20);
        numbers.add(30);

        // Print the list
        System.out.println("Numbers: " + numbers);
    }
}